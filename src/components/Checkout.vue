<template>
<div class="row">
  <div class="col-12">
    <div class="m-2">
      <div class="form-group m-2">
        <label for="">Name</label>
        <input type="text" class="form-controll" v-model="$v.order.name.$model" />
        <validation-error :validation="$v.order.name" />
        </div>
        <div class="form-group m-2">
        <label for="">E-mail</label>
        <input type="text" class="form-controll" v-model="$v.order.email.$model" />
        <validation-error :validation="$v.order.email" />
        </div>
        <div class="form-group m-2">
        <label for="">Address</label>
        <input type="text" class="form-controll" v-model="$v.order.address.$model" />
        <validation-error :validation="$v.order.address" />
        </div>
      </div>
    </div>

    <div class="col-12 text-center">
      <router-link to="/cart" class="btn btn-secondary m-1">Back</router-link>
      <button class="btn btn-primary m-1" @click="submitOrder">Place Order</button>
    </div>
  </div>
</template>

<script>
import { mapActions, mapGetters, mapMutations, mapState } from 'vuex';
import ValidationError from "ValidationError";
import {required, email} from 'vuelidate/lib/validators';

export default {
  components: {
    ValidationError
  },
  data() {
    return {
      order: {
        name: "John",
        email: "john@gmail.com",
        address: "John Street"
      }

    }
  },
  computed: {
    ...mapState({
      cart: state => state.cart.cart
    }),
    ...mapGetters({
      total: "cart/totalPrice"
    })
  },
  validations: {
    order: {
      name:{required},
      email:{required, email },
      address:{required},
    }
  },
  methods: {
    ...mapActions({
      storeOrder: "orders/storeOrderAction",
      cleanCartData: "cart/clearCartData"
    }),
    async submitOrder() {
      this.$v.$touch();
      if(!this.$v.$invalid) {
        const order = new FormData();

        order.append("Name", this.order.name);
        order.append("Email", this.order.email);
        order.append("Adress", this.order.address);
        order.append("Cart", JSON.stringify(this.cart));
        order.append("Total", this.total);

        await this.storeOrder(order);
        this.cleanCartData();
        this.$router.push("/thanks");
      }
    }
  },
};
</script>

